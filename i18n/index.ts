// Initialize i18n IMMEDIATELY and SYNCHRONOUSLY
// This prevents "NO_I18NEXT_INSTANCE" warning
i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: Localization.getLocales()[0]?.languageCode || 'en',
    fallbackLng: 'en',
    interpolation: {
      escapeValue: false
    }
  });

// Asynchronously load saved language
const loadSavedLanguage = async () => {
  try {
    const savedLanguage = await AsyncStorage.getItem('user-language');
    if (savedLanguage && savedLanguage !== i18n.language) {
      await i18n.changeLanguage(savedLanguage);
    }
  } catch (error) {
    console.warn('I18n: Failed to load language from AsyncStorage:', error);
  }
};

// Listen for language changes and persist them
i18n.on('languageChanged', (lng) => {
  AsyncStorage.setItem('user-language', lng).catch(err => {
    console.warn('I18n: Failed to save language to AsyncStorage:', err);
  });
});

loadSavedLanguage();

export default i18n;
